
-- Animorph Cards TCG Database Schema
-- MariaDB version 10.6.22
-- NFT Contract: 0xb08882e1804B444171B560Cf7cEe99aDD26f7f62

-- Users table
CREATE TABLE IF NOT EXISTS users (
    id INT AUTO_INCREMENT PRIMARY KEY,
    eos_id VARCHAR(64) UNIQUE NOT NULL,
    email VARCHAR(255),
    wallet_address VARCHAR(42),
    has_battle_pass BOOLEAN DEFAULT FALSE,
    battle_pass_expires DATE,
    created_at TIMESTAMP DEFAULT CURRENT_TIMESTAMP,
    updated_at TIMESTAMP DEFAULT CURRENT_TIMESTAMP ON UPDATE CURRENT_TIMESTAMP,
    INDEX idx_eos_id (eos_id),
    INDEX idx_wallet (wallet_address)
);

-- Animorph cards table (200 cards total)
CREATE TABLE IF NOT EXISTS animorph_cards (
    token_id INT PRIMARY KEY,
    contract_address VARCHAR(42) NOT NULL DEFAULT '0xb08882e1804B444171B560Cf7cEe99aDD26f7f62',
    nft_name VARCHAR(100) NOT NULL,
    display_name VARCHAR(120) NOT NULL, -- With element prefix for duplicates
    card_image_filename VARCHAR(150) NOT NULL,
    animorph_type ENUM('Fire','Water','Ice','Ground','Electric') NOT NULL,
    power_rating BIGINT NOT NULL,
    health BIGINT NOT NULL,
    attack BIGINT NOT NULL,
    sats BIGINT NOT NULL,
    size BIGINT NOT NULL,
    created_at TIMESTAMP DEFAULT CURRENT_TIMESTAMP,
    INDEX idx_element (animorph_type),
    INDEX idx_token_id (token_id),
    INDEX idx_power (power_rating),
    CONSTRAINT chk_token_id CHECK (token_id BETWEEN 1 AND 201 AND token_id != 43)
);

-- User card ownership table (tracks actual NFT ownership)
CREATE TABLE IF NOT EXISTS user_cards (
    user_id INT,
    token_id INT,
    quantity INT DEFAULT 1,
    last_synced TIMESTAMP DEFAULT CURRENT_TIMESTAMP ON UPDATE CURRENT_TIMESTAMP,
    acquired_at TIMESTAMP DEFAULT CURRENT_TIMESTAMP,
    PRIMARY KEY(user_id, token_id),
    FOREIGN KEY(user_id) REFERENCES users(id) ON DELETE CASCADE,
    FOREIGN KEY(token_id) REFERENCES animorph_cards(token_id) ON DELETE CASCADE,
    INDEX idx_user_id (user_id),
    INDEX idx_last_synced (last_synced)
);

-- Transactions table
CREATE TABLE IF NOT EXISTS transactions (
    id INT AUTO_INCREMENT PRIMARY KEY,
    user_id INT NOT NULL,
    type ENUM('deck','battle_pass') NOT NULL,
    amount DECIMAL(8,2) NOT NULL,
    currency CHAR(3) DEFAULT 'ZAR',
    yoco_id VARCHAR(64),
    status ENUM('pending','completed','failed') DEFAULT 'pending',
    created_at TIMESTAMP DEFAULT CURRENT_TIMESTAMP,
    FOREIGN KEY(user_id) REFERENCES users(id) ON DELETE CASCADE,
    INDEX idx_user_id (user_id),
    INDEX idx_yoco_id (yoco_id),
    INDEX idx_status (status)
);

-- Battles table
CREATE TABLE IF NOT EXISTS battles (
    id INT AUTO_INCREMENT PRIMARY KEY,
    player1_id INT NOT NULL,
    player2_id INT,
    battle_state JSON,
    result ENUM('win','lose','draw','ongoing') DEFAULT 'ongoing',
    winner_id INT,
    played_at TIMESTAMP DEFAULT CURRENT_TIMESTAMP,
    FOREIGN KEY(player1_id) REFERENCES users(id) ON DELETE CASCADE,
    FOREIGN KEY(player2_id) REFERENCES users(id) ON DELETE SET NULL,
    FOREIGN KEY(winner_id) REFERENCES users(id) ON DELETE SET NULL,
    INDEX idx_player1 (player1_id),
    INDEX idx_player2 (player2_id),
    INDEX idx_played_at (played_at)
);

-- Leaderboards table
CREATE TABLE IF NOT EXISTS leaderboards (
    user_id INT PRIMARY KEY,
    lbp_points INT DEFAULT 0,
    total_wins INT DEFAULT 0,
    total_matches INT DEFAULT 0,
    last_updated TIMESTAMP DEFAULT CURRENT_TIMESTAMP ON UPDATE CURRENT_TIMESTAMP,
    FOREIGN KEY(user_id) REFERENCES users(id) ON DELETE CASCADE,
    INDEX idx_lbp_points (lbp_points DESC),
    INDEX idx_total_wins (total_wins DESC)
);

-- Songs table
CREATE TABLE IF NOT EXISTS songs (
    id INT AUTO_INCREMENT PRIMARY KEY,
    title VARCHAR(255) NOT NULL,
    file_path VARCHAR(255) NOT NULL,
    duration INT NOT NULL COMMENT 'Duration in seconds',
    category ENUM('good','kids&teens','great','ok_songs') DEFAULT 'good',
    created_at TIMESTAMP DEFAULT CURRENT_TIMESTAMP,
    INDEX idx_category (category),
    INDEX idx_title (title)
);

-- User songs table (for free user selections)
CREATE TABLE IF NOT EXISTS user_songs (
    user_id INT,
    song_id INT,
    selected_at TIMESTAMP DEFAULT CURRENT_TIMESTAMP,
    PRIMARY KEY(user_id, song_id),
    FOREIGN KEY(user_id) REFERENCES users(id) ON DELETE CASCADE,
    FOREIGN KEY(song_id) REFERENCES songs(id) ON DELETE CASCADE
);

